package quizzer;

public class QuizUser {

	private int QU_ID;
	private String QU_LOGIN;
	private String QU_ROLE;
	
	QuizUser(){}

	/**
	 * @return the qU_ID
	 */
	public int getQU_ID() {
		return QU_ID;
	}

	/**
	 * @return the qU_LOGIN
	 */
	public String getQU_LOGIN() {
		return QU_LOGIN;
	}

	/**
	 * @return the qU_ROLE
	 */
	public String getQU_ROLE() {
		return QU_ROLE;
	};
	
	
}
